public class ProgrammingExercise10 {
    public static void main(String[] args) {
        /*
            Assume that a runner runs 14 kilometers in 45 minutes and 30 seconds.
            Write a program that displays the average speed in miles per hour.
            (Note 1 mile is equal to 1.6 kilometers.)
         */

        System.out.println("14 kilometers to miles is " + (14 / 1.6) + ".");
        System.out.println("45 minutes and 30 seconds to hour is " + (45.5 / 60) + ".");
        System.out.println("Runner runs " + (8.75 / 0.7583333333333333) + " miles per hour.");
    }
}
